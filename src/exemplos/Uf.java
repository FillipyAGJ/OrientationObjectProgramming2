package exemplos;

public class Uf {
    private String nome;
    private int qtdMunicipio;
    private long populacao;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getQtdMunicipio() {
        return qtdMunicipio;
    }

    public void setQtdMunicipio(int qtdMunicipio) {
        this.qtdMunicipio = qtdMunicipio;
    }

    public long getPopulacao() {
        return populacao;
    }

    public void setPopulacao(long populacao) {
        this.populacao = populacao;
    }
}
