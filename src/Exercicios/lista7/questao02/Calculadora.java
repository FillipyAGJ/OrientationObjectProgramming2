package Exercicios.lista7.questao02;

public class Calculadora {
    
    public static int somaDoisNumeros(int num, int num2) {
        return (num + num2);
    }

    public static int somaTresNumeros(int num, int num2, int num3) {
        return (num + num2 + num3);
    }

    public static int subtraiDoisNumeros(int num, int num2) {
        return (num - num2);
    }

    public static int subtraiTresNumeros(int num, int num2, int num3) {
        return (num - num2 - num3);
    }

    public static int multiplicaDoisNumeros(int num, int num2) {
        return (num * num2);
    }

    public static int multiplicaTresNumeros(int num, int num2, int num3) {
        return (num * num2 * num3);
    }

    public static int divideDoisNumeros(int num, int num2) {
        return (num / num2);
    }

    public static int divideTresNumeros(int num, int num2, int num3) {
        return (num / num2 / num3);
    }
}
